    private void b_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_simpanActionPerformed
        // TODO add your handling code here:
        class_registrasi regis = new class_registrasi();

        regis.setIdRegistrasi(lbl_id.getText());
        regis.setIdKegiatan(idKegiatan(lbl_Kegiatan.getText()));
        try {
            for (String[] item : daftarAnggota) {
                String namaAnggota = item[1];

                try {
                    String sql = "SELECT * FROM anggota WHERE nama_anggota=?";
                    Connection con = koneksi.konek();
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setString(1, namaAnggota);
                    ResultSet rs = ps.executeQuery();

                    if (rs.next()) {
                        String idAnggota = rs.getString("ID_anggota");
                        regis.setIdAnggota(idAnggota);
                        regis.simpanRegis();

                    }
                    rs.close();
                    ps.close();
                    con.close();
                    
                } catch (SQLException e) {
                    //jika error. kembali string kosong
                    System.out.println(e);
                }
            }
            // ðŸ§¹ Reset daftarAnggota agar kosong untuk input berikutnya
                    daftarAnggota.clear();

                    // ðŸ§¹ Reset tampilan tabel (hapus semua baris)
                    DefaultTableModel model = (DefaultTableModel) table_peserta.getModel();
                    model.setRowCount(0);
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
        
    }//GEN-LAST:event_b_simpanActionPerformed

