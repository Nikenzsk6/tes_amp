    private void table_eventMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_eventMouseClicked
        // TODO add your handling code here:
        int baris = table_event.rowAtPoint(evt.getPoint());

        //pengecekkan tanggal
        if (baris >= 0) {
            String idKegiatan = table_event.getValueAt(baris, 1).toString();
            String namaKegiatan = table_event.getValueAt(baris, 2).toString(); // kolom namaKegiatan
            String tglMulaiStr = table_event.getValueAt(baris, 3).toString(); // kolom tgl mulai
            String tglSelesaiStr = table_event.getValueAt(baris, 4).toString(); // kolom tgl selesai
            String lokasi = table_event.getValueAt(baris, 5).toString(); // kolom tgl selesai
            String parentUjian = table_event.getValueAt(baris, 6).toString(); // kolom tgl selesai

            String[] options = {"Ubah Data", "Tambah Peserta", "Batal"};

            int choice = JOptionPane.showOptionDialog(
                    null,
                    "Pilih aksi yang ingin dilakukan:",
                    "Konfirmasi",
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    options,
                    options[0]
            );

            if (choice == 0) {
                // Aksi jika pilih Ubah Data
                popUp_tambah_data_registrasi regis = new popUp_tambah_data_registrasi(this);
                regis.setVisible(true);
                regis.tampil_data(idKegiatan, namaKegiatan, tglMulaiStr, tglMulaiStr, lokasi, parentUjian);
                regis.tampilan_edit_regist();
            } else if (choice == 1) {
                // Aksi jika pilih Tambah Peserta
                try {
                // Format tanggal dari database (biasanya yyyy-MM-dd)
                SimpleDateFormat sdfDB = new SimpleDateFormat("yyyy-MM-dd");
                Date tglMulai = sdfDB.parse(tglMulaiStr);
                Date tglSelesai = sdfDB.parse(tglSelesaiStr);

                // Ambil tanggal hari ini dari lbl_tgl (format dd-MM-yyyy)
                SimpleDateFormat sdfNow = new SimpleDateFormat("dd-MM-yyyy");
                Date tanggalSekarang = sdfNow.parse(currentDate);

                // Bandingkan tanggal
                if (!tanggalSekarang.before(tglMulai) && !tanggalSekarang.after(tglSelesai)) {
                    // Hari ini di antara tgl_mulai dan tgl_selesai
                    popUp_tambah_registrasi tambah_regis = new popUp_tambah_registrasi(idKegiatan);
                    tambah_regis.setVisible(true);
                    tambah_regis.namaKegiatan(namaKegiatan); // set nama kegiatan di panel tambah registrasi
                    JOptionPane.showMessageDialog(null,
                            "Periode registrasi masih berlangsung sampai " + tglSelesaiStr);
                } else if (tanggalSekarang.before(tglMulai)) {
                    // Hari ini sebelum tgl_mulai 
                    /*popUp_data_registrasi data_regis = new popUp_data_registrasi();
                    data_regis.setVisible(true); */
                    JOptionPane.showMessageDialog(null,
                            "Periode registrasi belum dimulai.");
                } else if (tanggalSekarang.after(tglSelesai)) {
                    // Hari ini setlah tgl_selesai 
                    popUp_data_registrasi data = new popUp_data_registrasi();
                    data.tampilDataAKhir(idKegiatan);
                    data.setVisible(true);
                    JOptionPane.showMessageDialog(null,
                            "Periode registrasi sudah berakhir.");

                }

            } catch (Exception e) {
                System.out.println(e);
            }
                
            } else {
                // choice == 2 atau dialog ditutup
                // Tidak melakukan apapun â†’ otomatis menutup
//                System.out.println("Dialog ditutup tanpa aksi");
            }  
        }
    }//GEN-LAST:event_table_eventMouseClicked

